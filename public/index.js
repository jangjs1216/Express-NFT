  function transfer()
  {
    var account = document.getElementById('InputAccount').value;
    var to = document.getElementById('toAddress').value;
    var tokenId;
    
    contract.methods.getTokenIdByOwner().call().then(msg => {
      tokenId = msg[0];

      return contract.methods.transfer(to, tokenId)
      .send({ from: account, gas: 3000000, gasPrice: 3000000 })
      .on("receipt", function(receipt) {
        console.log("Transaction Success!");
      })
      .on("error", function(error) {
        console.log("Transaction Failed!");
        console.log(error);
      });
    });
  }

